

public class Employee {
	private String name;
	private String sex;
	private String age;

	public void setName(String name) {
		this.name = name;	
	}

	public void setSex(String sex) {
		this.sex = sex;	
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getName() {
		return this.name;
	}

	public String getSex() {
		return this.sex;
	}

	public String getAge() {
		return this.age;
	}

}
